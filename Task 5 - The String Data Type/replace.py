# declare and store variable

sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog!."

# print variable
print(sentence)

# replace ! with blank space (This leaves a space before the fullstop, and I have not deleted)
print(sentence.replace("!"," "))

# reprint with uppercase
print(sentence.replace("!"," ").upper())

# reprint in reverse
print(sentence[::-1])
